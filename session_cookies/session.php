session_start();-Ця функція перевіряє, чи існує ідентифікатор сесії,
і, якщо немає, то створює його. Якщо ідентифікатор поточної сесії вже існує, 
то завантажуються зареєстровані змінні сесії.
$_SESSION-суперглобальний масив, в якому зберігається інформація сесії.


	// Инициируем сессию
	session_start();
	// Помещаем значение в сессию
	$_SESSION['name'] = "value";
	// Помещаем массив в сессию
	$arr = array("first", "second", "third");
	$_SESSION['arr'] = $arr;
	// Выводим ссылку на другую страницу
	echo "<a href='other.php'>другая страница</a>";

Вміст сторінки other.php:
	 // Инициируем сессию
	  session_start();
	  // Выводим содержимое суперглобального массива $_SESSION
	  echo "<pre>";
	  print_r($_SESSION);
	  echo "</pre>";

Результат роботи:
Array
(
    [name] => value
    [arr] => Array
        (
            [0] => first
            [1] => second
            [2] => third
        )
)

